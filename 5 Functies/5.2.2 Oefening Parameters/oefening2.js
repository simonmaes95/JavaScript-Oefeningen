function berekenLengte(naam1, naam2) {
    let totaleLengte = naam1.length + naam2.length;
    console.log("De totale lengte van de namen is " + totaleLengte + " tekens.");
}

berekenLengte("Alice", "Bob"); // Output: De totale lengte van de namen is 8 tekens.
berekenLengte("Charlie", "David"); // Output: De totale lengte van de namen is 12 tekens.
